;BUBBLE SORT

.MODEL SMALL

.DATA
DIRECTION EQU 1     ;1 FOR ASCENDIGN AND 0 FOR DESCENDING ORDER
NUMS DB 1,2,3,4,5
COUNT DW ($-NUMS)/TYPE NUMS

.CODE

MOV AX,@DATA
MOV DS,AX

MOV BX,COUNT
DEC BX

OUTER:
LEA SI, NUMS
MOV CX,BX
INNER:
MOV AL,[SI]
ADD SI,TYPE NUMS
CMP AL,[SI]
IF DIRECTION
  JB SKIP
ELSE
  JA SKIP
ENDIF
XCHG AL,[SI]
MOV [SI-TYPE NUMS],AL

SKIP: LOOP INNER
DEX BX
JNZ OUTER

MOV AH,4CH
INT 21H

END
