;LINEAR SEARCH

.MODEL SMALL

.DATA

NUMS DB 10H,20H,30H,40H,50H,60H,70H,80H,90H,92H
KEY DB 50H
POS DB '0','$'

MSG1 DB 10,13,  "KEY FOUND AT: $"
MSG2 DB 10,13,  "KEY NOT FOUND $"

.CODE
MOV AX,@DATA
MOV DS,AX

MOV SI,OFFSET NUMS ; pointer pointing at the starting of the array NUMS
MOV CX,COUNT
MOV AL,KEY

CONT:

CMP AL,[SI]
JE FOUND

INC SI
INC POS
DEC CX

JNZ CONT

FOUND:

LEA DX,MSG1
MOV AH,09H          ; Interrupt to print string available in DX register
INT 21H

LEA DX,POS
MOV AH,09
INT 21H

EXIT:
MOV AH,4CH
INT 21H

END


