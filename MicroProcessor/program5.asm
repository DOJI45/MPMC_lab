; STRING OPERATIONS

; MACRO FOR DISPLAYING A MESSAGE(STRING)

DISPSTR MACRO MSG
LEA DX,MSG
MOV AH,09H
INT 21H
ENDM

.MODEL SMALL

.DATA

STR1 DB 100,?,100 DUP('$')
STR2 DB 100,?,100 DUP('$')

MSG1 DB 10,13,"ENTER THE STRING 1: $"
MSG2 DB 10,13,"ENTER THE STRING 2: $"
MSG3 DB 10,13,"STRINGS ARE EQUAL$"
MSG4 DB 10,13,"STRINGS ARE NOT EQUAL$"

MSG5 DB 10,13,"LENGHT OF THE STRING 1 IS = "
LEN1 DB "$$$"
MSG6 DB 10,13,"LENGHT OF THE STRING 2 IS = "
LEN2 DB "$$$"


.CODE
MOV AX,@DATA
MOV DS,AX
MOV ES,AX

DISP MSG1

LEA DX,STR1
MOV AH,10
INT 21H

DISP MSG2

LEA DX,STR2
MOV AH,10
INT 21H

MOV AL,STR1+1
CMP AL,STR2+1   ; STR+ 1 HOLDS THE LENGTH OF THE STRING

JNE NOTEQUAL

LEA SI, STR1+2
LEA DI, STR2+2

MOV CH,00
MOV CL, STR1+ 1

CLD
REPE CMPSB  ; REPEAT ON EQUAL COMPARE STRING BYTES [SI] AND [DI]

JNE NOTEQUAL

DISP MSG3
JMP EXIT

NOTEQUAL:
DISP MSG4

EXIT:
MOV AL, STR1+ 1
AAM                   ; ASCII ADJUST
ADD AX, 3030H
MOV LEN1, AH
MOV LEN1+1, AL

DISP MSG5

MOV AL, STR2+1
AAM
ADD AX,3030H
MOV LEN2,AH
MOV LEN2+1, AL
DISP MSG6

MOV AH,4CH
INT 21H

END


